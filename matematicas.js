export function sumar(num1, num2) {
  return num1 + num2;
}
export function restar(num1, num2) {
  return num1 - num2;
}
export function multi(num1, num2) {
  return num1 * num2;
}
export function div(num1, num2) {
  return num1 / num2;
}
export function modulo(num1, num2) {
  return num1 % num2;
}

export function ordenar(num1, num2, num3) {
  let numeros = [num1, num2, num3];

  numeros.sort();
  return numeros;
}
